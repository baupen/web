namespace App\Api\Entity\Base;

class Map extends BaseEntity
{
    /**
     * @var string
     */
    private $name;

    /**
     * @return string
     */
    public function isName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}
